public class Roma2Latin {
    public static int r2L(String number) {
        int result = 0;
        switch (number) {
            case ("I"):
                result = 1;
                break;
            case ("II"):
                result = 2;
                break;
            case ("III"):
                result = 3;
                break;
            case ("IV"):
                result = 4;
                break;
            case ("V"):
                result = 5;
                break;
            case ("VI"):
                result = 6;
                break;
            case ("VII"):
                result = 7;
                break;
            case ("VIII"):
                result = 8;
                break;
            case ("IX"):
                result = 9;
                break;
            case ("X"):
                result = 10;
                break;

        }
        return result;
    }
}
